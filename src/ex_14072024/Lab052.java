public class Lab052 {
    public static void main(String[] args) {
        // Difference between = and ==
        int a = 11;             // assigns the value
        boolean b = (10==11);   // compare between 2 values - result will only be on boolean - true/false

        System.out.println(a);  // value will be printed
        System.out.println(b);  // either true or false will be printed

        // === is not present in JAVA, presents only in JavaScript

    }
}
