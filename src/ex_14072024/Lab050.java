package ex_14072024;

public class Lab050 {
    public static void main(String[] args) {
        //Operators - Unary Operator
        // data_type variable_name = variable_value
        int YOB = 1993;
        //Unary --> only one operand, operator
        //here YOB is the Operand and = is the Operator.
        // here by default the value of the Unary Operator is Plus (+)
        System.out.println("My Year of Birth is: " + YOB);
        System.out.printf("My Year Of Birth is: %d\n",YOB);
        int SUV = -1;
        //Unary --> only one operand, operator
        //here SUV is the Operand and = is the Operator.
        // here the value of the Unary Operator is Minus (-)
        System.out.println("My First car would be: " + SUV);
        System.out.printf("My First car would be: %d\n",SUV);


        //Binary Operator
        int a = 11;
        int b = 4;
        int sum = a+b;
        //binary - arithmetic operator (+, -, *, /, %)
        // here a and b are the two operands
        // + (plus) is the operator
        System.out.println("The Sum of a and b is: " + sum);
        System.out.printf("The sum of a and b is: %d\n", sum);


        //ternary Operator
        //here a,b,c are the operands




    }
}
