package ex_13072024;

public class Lab045 {
    public static void main(String[] args) {
        // relational operators: Answer will be always boolean only (true or false)
        // <, >, <=, >=, ==, !=, (!=)
        // in this lab, we will see about ==, !=
        System.out.println(10==10);
        System.out.println(10<=10);
        System.out.println(10>=10);
        System.out.println(10<10);
        System.out.println(10>10);
        System.out.println(10!=10);
    }
}
