package ex_13072024;

public class Lab027 {
    public static void main(String[] args) {
        int decimal = 10;     // This is a decimal number system
        int binary = 0b1010; // This is binary number system - include 0b (zero and b) to specify the binary number
        int octal = 0101;   // this is octal number system
        int hexa = 0Xface;   // this is hexa number system

        System.out.println(decimal);
        System.out.println(binary);
        System.out.println(octal);
        System.out.println(hexa);

        // This can be done using Rapid Tables - https://www.rapidtables.com/convert/number/decimal-to-octal.html?x=10

    }
}
