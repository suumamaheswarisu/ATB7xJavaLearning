package ex_13072024;

public class Lab035 {
    public static void main(String[] args) {
        //guide to add ESCAPE CHARACTERS: new line, tab, delete a char and delete a word
        char c = '\n';  // to add a new line
        char c1 = '\t'; //to add a tab
        char c2 = '\b'; //to delete a char
        char c3 = '\r'; //to delete a word
        System.out.println("Uma" + c + "Maheswari");
        System.out.println(c);
        System.out.println("Uma" + c1 + "Maheswari");
        System.out.println(c1);
        System.out.println("Uma" + c2 + "Maheswari");
        System.out.println("Uma" + c3 + "Maheswari");
        /**
         * inorder to insert ' - we need to use \'
         * inorder to insert " - we need to use \"
         * inorder to insert \ - we need to use \\
         */

    }
}
